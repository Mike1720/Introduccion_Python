# Variables
"""
    Las variables son espacios de memoria que se utilizan para almacenar datos.
    En Python no es necesario declarar el tipo de variable, debido a que es un 
        lenguaje de tipado dinamico.
    Cada variable tiene un nombre unico y un valor asociado.
    Para asignar un valor a una variable se utiliza el operador de asignación (=).
    Existen convenciones para nombrar variables:
    - No pueden COMENZAR con números.
    - No pueden contener espacios.
    - No pueden contener carácteres especiales, excepto guión bajo (_).
    - No pueden ser palabras reservadas del lenguaje.
    - Se recomienda utilizar nombres descriptivos.
    - Se recomienda utilizar convenciones de nomenclatura como:
        - snake_case: palabras separadas por guiones bajos
        - camelCase: primera palabra en minúscula y las siguientes en mayúscula.
        - PascalCase: todas las palabras en mayúscula.
        - SCREAMING_SNAKE_CASE: todas las palabras en mayúscula y separadas por guines bajos.
"""

